                        var modal6 = document.getElementById("myModal6");
                        var btn6 = document.getElementById("openModalBtn6");
                        var span6 = document.getElementsByClassName("close6")[0];
                        var dropdownBtn6 = document.getElementById("myBtn6");
                        var dropdownContent6 = document.getElementById("dropdownContent6");

                        btn6.onclick = function() {
                            modal6.style.display = "block";
                        }

                        span6.onclick = function() {
                            modal6.style.display = "none";
                        }

                        window.onclick = function(event) {
                            if (event.target == modal6) {
                                modal6.style.display = "none";
                            }
                        }

                        dropdownBtn6.onclick = function() {
                            if (dropdownContent6.style.display === "block") {
                                dropdownContent6.style.display = "none";
                            } else {
                                dropdownContent6.style.display = "block";
                            }
                        }



